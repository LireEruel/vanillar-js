export default function colorFlip(isSimpleMode) {
  if (isSimpleMode) {
    return simpleColorList[Math.floor(Math.random() * simpleColorList.length)];
  } else {
    const letters = "0123456789ABCDEF";
    let color = "#";
    // 6자리를 랜덤으로 생성합니다.
    for (let i = 0; i < 6; i++) {
      color += letters[Math.floor(Math.random() * 16)];
    }
    return color;
  }
}
